/*
 * hb_font.c — ASCII text rendering using a built-in 8x8 bitmap font.
 *
 * Font glyphs (96 chars, 0x20..0x7F) extracted from Linux's
 * lib/fonts/font_8x8.c (GPL-2.0). Confined to this file so license
 * implications stay localized.
 *
 * hb_draw_char(x, y, c, scale, fg, bg) draws one glyph at the given
 * integer pixel scale (1 = 8x8, 2 = 16x16, 3 = 24x24, ...). Each row
 * is pushed as a single MIPI long-write strip for speed.
 *
 * For a string, use hb_draw_str(x, y, s, scale, fg, bg).
 */

#include "hb_sdk.h"

/* Forward decls from hb_mipi.c (we share its MMIO helpers). */
extern void hb_mipi_set_window(int16_t x, int16_t y, int16_t w, int16_t h);
extern void hb_screenshot_shadow_text_strip(uint8_t row_byte, uint8_t scale,
                                            hb_color_t fg, hb_color_t bg);

/* Local copy of the MMIO + helpers. Duplicates a few constants from
   hb_mipi.c to keep that file unchanged. */
#define _MIPI_BASE     0x3d800000u
#define _DSIM_CONFIG   (*(volatile uint32_t *)(_MIPI_BASE + 0x10))
#define _DSIM_PKTHDR   (*(volatile uint32_t *)(_MIPI_BASE + 0x34))
#define _DSIM_PAYLOAD  (*(volatile uint32_t *)(_MIPI_BASE + 0x38))
#define _DSIM_FIFOCTRL (*(volatile uint32_t *)(_MIPI_BASE + 0x44))
#define _DSIM_FullLSfr  (1u << 21)
#define _DSIM_EmptyHSfr (1u << 22)
#define _DSIM_EmptyLSfr (1u << 20)
#define _DSIM_TYPE_LONG_WRITE 0x29

static inline void _wait_long_done(void) {
    for (uint32_t t = 0; t < 50000000u; t++) {
        if ((_DSIM_FIFOCTRL & (_DSIM_EmptyHSfr | _DSIM_EmptyLSfr))
            == (_DSIM_EmptyHSfr | _DSIM_EmptyLSfr)) return;
        __asm__ volatile("nop");
    }
}
static inline void _wait_payload_space(void) {
    for (uint32_t t = 0; t < 10000000u; t++) {
        if (!(_DSIM_FIFOCTRL & _DSIM_FullLSfr)) return;
        __asm__ volatile("nop");
    }
}

/* Push one row of a glyph as (8*scale) pixels wide, (scale) pixels
   tall. Replicates the byte horizontally and vertically per scale. */
static void push_char_strip(uint8_t cmd, uint8_t row_byte, uint8_t scale,
                            hb_color_t fg, hb_color_t bg)
{
    hb_screenshot_shadow_text_strip(row_byte, scale, fg, bg);

    uint32_t strip_pixels = 8u * scale * scale; /* width(8*scale) * height(scale) */
    uint32_t total_len = 1u + strip_pixels * 3u;
    _DSIM_CONFIG &= ~(1u << 28);

    uint32_t payload = 0; uint32_t i = 0;
    uint8_t fr=(fg>>16), fgg=(fg>>8), fb=(uint8_t)fg;
    uint8_t br=(bg>>16), bgg=(bg>>8), bb=(uint8_t)bg;

    payload = (payload >> 8) | ((uint32_t)cmd << 24); i++;

    for (uint8_t sub_v = 0; sub_v < scale; sub_v++) {
        for (int bit = 7; bit >= 0; bit--) {
            int on = (row_byte >> bit) & 1;
            uint8_t r = on ? fr  : br;
            uint8_t g = on ? fgg : bgg;
            uint8_t b = on ? fb  : bb;
            for (uint8_t sc = 0; sc < scale; sc++) {
                payload = (payload >> 8) | ((uint32_t)r << 24);
                if ((i & 3) == 3) { _wait_payload_space(); _DSIM_PAYLOAD = payload; } i++;
                payload = (payload >> 8) | ((uint32_t)g << 24);
                if ((i & 3) == 3) { _wait_payload_space(); _DSIM_PAYLOAD = payload; } i++;
                payload = (payload >> 8) | ((uint32_t)b << 24);
                if ((i & 3) == 3) { _wait_payload_space(); _DSIM_PAYLOAD = payload; } i++;
            }
        }
    }
    uint32_t rem = i & 3;
    if (rem) {
        _wait_payload_space();
        if (rem == 3)      _DSIM_PAYLOAD = (payload >> 8);
        else if (rem == 2) _DSIM_PAYLOAD = (payload >> 16);
        else if (rem == 1) _DSIM_PAYLOAD = (payload >> 24);
    }
    _DSIM_PKTHDR = _DSIM_TYPE_LONG_WRITE | ((total_len & 0xFFFFu) << 8);
    _wait_long_done();
}

