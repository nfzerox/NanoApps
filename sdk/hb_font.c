void hb_draw_char(int16_t x, int16_t y, char c, uint8_t scale,
                  hb_color_t fg, hb_color_t bg)
{
    if (scale == 0) scale = 1;
    uint8_t ix;
    if ((unsigned char)c < 0x20 || (unsigned char)c > 0x7F) ix = 0;
    else ix = (unsigned char)c - 0x20;

    int16_t w = 8 * scale;
    int16_t h = 8 * scale;
    hb_mipi_set_window(x, y, w, h);
    for (int row = 0; row < 8; row++) {
        push_char_strip(row == 0 ? 0x2C : 0x3C,
                        hb_font8x8[ix][row], scale, fg, bg);
    }
}

void hb_draw_str(int16_t x, int16_t y, const char *s, uint8_t scale,
                 hb_color_t fg, hb_color_t bg)
{
    if (scale == 0) scale = 1;
    int16_t cw = 8 * scale;
    while (*s) {
        hb_draw_char(x, y, *s++, scale, fg, bg);
        x += cw;
    }
}
